function areCoachesJudging() {
	/*
	 determine whether coaches will judge
	This is done by counting number of present, non-coach judges,
	and seeing if there are at least number of rounds * 2 judges
	*/
	let numberOfJudges = 0;
	for (let a = 0; a < judges.length; a++) {
		if (!judges[a].isCoach && judges[a].isPresent) {
			numberOfJudges++;
		}
	}
	if (numberOfJudges < (pairings.length * 2)) {
		return true;
	} else {
		return false;
	}
}

function getRoundJudges() {
	/*
	Filter judge list to remove judges who are not present
	Also filter out coaches if coaches are not judging
	*/
	let roundJudges = [];
	let coachesAreJudging = areCoachesJudging();
	for (let a = 0; a < judges.length; a++) {
		if (coachesAreJudging) {
			if (judges[a].isPresent) {
				roundJudges.push(judges[a])
			}
		} else {
			if (judges[a].isPresent && !judges[a].isCoach) {
				roundJudges.push(judges[a])
			}
		}

	}
	return roundJudges;
}

